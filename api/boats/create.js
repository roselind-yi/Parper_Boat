import { createBoat } from '../../lib/db.js';
import { authenticate } from '../../lib/jwt.js';

export default async function handler(req) {
  if (req.method !== 'POST') {
    return new Response(JSON.stringify({ error: 'Method not allowed' }), {
      status: 405,
      headers: { 'Content-Type': 'application/json' },
    });
  }

  try {
    const user = await authenticate(req);

    if (!user) {
      return new Response(JSON.stringify({ error: 'Unauthorized' }), {
        status: 401,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    const { content, pathType, latitude, longitude } = await req.json();

    if (!content || !pathType) {
      return new Response(JSON.stringify({ error: 'Missing required fields' }), {
        status: 400,
        headers: { 'Content-Type': 'application/json' },
      });
    }

    const boat = await createBoat(user.id, content, pathType, latitude, longitude);

    return new Response(JSON.stringify({
      id: boat.id,
      content: boat.content,
      path_type: boat.pathType,
      status: boat.status,
      created_at: boat.createdAt,
    }), {
      status: 201,
      headers: { 'Content-Type': 'application/json' },
    });
  } catch (error) {
    return new Response(JSON.stringify({ error: error.message }), {
      status: 500,
      headers: { 'Content-Type': 'application/json' },
    });
  }
}
